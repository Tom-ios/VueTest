<template>
  <el-breadcrumb class="app-breadcrumb" separator="/">
    <transition-group name="breadcrumb">
      <el-breadcrumb-item v-for="(item,index) in levelList" :key="item.name">
        <span v-if="item.redirect==='noRedirect'||index==levelList.length-1" class="no-redirect">{{ item.meta.title }}</span>
        <a v-else @click.prevent="handleLink(item)">{{ item.meta.title }}</a>
      </el-breadcrumb-item>
    </transition-group>
  </el-breadcrumb>
</template>

<script>
import pathToRegexp from 'path-to-regexp'
import router from '@/router'
export default {
  data() {
    return {
      levelList: null
    }
  },
  watch: {
    $route() {
      this.getBreadcrumb()
    }
  },
  created() {
    this.getBreadcrumb()
  },
  methods: {
    getBreadcrumb() {
      this.levelList = []
      const item = this.$route
      const arr = item.meta && item.meta.title && item.meta.breadcrumb ? item.meta.breadcrumb.split(',') : []
      arr.push(item.name)
      arr.forEach(name => {
        const item = this.getItem(name)
        if (item) {
          this.levelList.push(item)
        }
      })
    },
    getItem(name) {
      for (const route of router.options.routes) {
        if (route.children && route.children.length > 0) {
          for (const child of route.children) {
            if (child.name === name) {
              return child
            }
          }
        }
      }
    },
    isDashboard(route) {
      const name = route && route.name
      if (!name) {
        return false
      }
      return name.trim().toLocaleLowerCase() === 'Dashboard'.toLocaleLowerCase()
    },
    pathCompile(path) {
      // To solve this problem https://github.com/PanJiaChen/vue-element-admin/issues/561
      const { params } = this.$route
      var toPath = pathToRegexp.compile(path)
      return toPath(params)
    },
    handleLink(item) {
      this.$router.push({ name: item.name, params: this.$route.params })
    }
  }
}
</script>

